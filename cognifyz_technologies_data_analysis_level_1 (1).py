plt.xlabel('Price Range')
plt.ylabel('Number of Restaurants')
plt.title('Distribution of Restaurants by Price Range')

# Display percentage labels on top of bars
for i, count in enumerate(price_counts.values):
    plt.text(i, count + 5, f'{price_percent.values[i]:.2f}%', ha='center')

plt.show()

"""# **ðŸ§¾ Level 1 â€“ Task 4: Online Delivery Analysis**


---


Objective:
This task aims to analyze how the availability of online delivery services impacts restaurant performance, specifically focusing on:


*   Determining the percentage of restaurants that offer online delivery.
*   Comparing the average ratings of restaurants with and without online delivery.



---


Why it matters:
With the increasing demand for online food ordering, itâ€™s important to understand if offering online delivery correlates with better customer satisfaction. This insight helps businesses decide whether to invest in online delivery infrastructure.

"""

import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

df = pd.read_csv("/content/drive/MyDrive/Dataset.csv")

df = df[df['Has Online delivery'].notnull()]

online_delivery_counts = df['Has Online delivery'].value_counts()
total_restaurants = len(df)
online_percentage = (online_delivery_counts['Yes'] / total_restaurants) * 100
offline_percentage = (online_delivery_counts['No'] / total_restaurants) * 100

print(f"Restaurants offering online delivery: {online_percentage:.2f}%")
print(f"Restaurants not offering online delivery: {offline_percentage:.2f}%")

online_ratings = df[df['Has Online delivery'] == 'Yes']['Aggregate rating'].mean()
offline_ratings = df[df['Has Online delivery'] == 'No']['Aggregate rating'].mean()

print(f"Average rating (Online Delivery): {online_ratings:.2f}")
print(f"Average rating (No Online Delivery): {offline_ratings:.2f}")

plt.figure(figsize=(6,4))
sns.barplot(x=['Online Delivery', 'No Online Delivery'], y=[online_ratings, offline_ratings], palette='coolwarm')
plt.ylabel("Average Rating")
plt.title("Comparison of Average Ratings")
plt.ylim(0, 5)
plt.show()